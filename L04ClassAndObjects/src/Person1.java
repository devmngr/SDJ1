
public class Person1

{
   
   
   private String name;
   private String birthday;
   
   
   public   Person1(String name, String birthday)
   {
     this.name = name;
     this.birthday = birthday;
   }
   
   
   public void setName(String name)
   { 
      this.name = name;
   }
   

   
   public String getName()
   {
      return name;
   }
   
   
   public String getBirthday()
   {
      return birthday;
   }
   
}



