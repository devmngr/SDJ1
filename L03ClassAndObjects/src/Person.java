
public class Person

{
   
   
   private String name;
   private String birthday;
   
   
   public void setName(String n)
   { 
      name = n;
   }
   
   
   public void setBirthday(String bDay)
   {
      birthday = bDay;
   }

   
   public String getName()
   {
      return name;
   }
   
   
   public String getBirthday()
   {
      return birthday;
   }
   
}



